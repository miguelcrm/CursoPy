# Cálculo do IMC (Indíce de Massa Corporal)

''' Elipses = ..., funciona como algo que ainda será escrito '''

nome = 'Miguel Camargo'
altura = 1.82
peso = 68

# IMC = peso / (altura x altura)

imc = peso / (altura ** 2)

print('Nome:', nome)
print('Altura:', altura)
print('Pesa:', peso)
print('E o seu IMC é:', imc)