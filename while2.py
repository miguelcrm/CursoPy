# Operadores de atribuição + While

'''
Podemos fazer da seguinte maneira, usando os operadores de atribuição.

'=' '+=' '-=' '*=' '/=' '//=' '**=' '%='

'''

contador = 0

while contador < 30: # Enquanto o contador for menor que 30
    contador += 1 # Adicione mais um
    print(contador) # Imprima contador

    




