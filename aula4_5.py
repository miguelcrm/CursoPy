# Tipo de dado bool (boolean)
# São estruturas de dados lógicas em um programa
# Só terá duas possibilidades, sim ou não
# Ou True e False 

print ( 10 == 10) # True
print ( 10 == 11) # False
print ( 10 > 7 )  # True
print ( 10 < 7 )  # False 