# INT E FLOAT

# O tipo INT recebe qualquer número positivo ou negativo
# Desde que seja inteiro como 10, 150, 1000.

print(11)
print(-11)
print(0)
print(1050)

# O tipo FLOAT representa qualquer número positivo ou negativo.
# Desde que contenha pontos flutuantes ou seja casas decimais.

print(1.1)
print(1245.67)
print(540.234)

# type()

# type te retorna o tipo de dado usado em seu código
# type é uma class.

print(type(123))
print(type("Miguel"))
print(type(1.1), type(10), type(10 > 40), type('Camargo'))


